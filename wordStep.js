'use strict'
function wordStep(str) {
    //code me
    var arr = [];
    var strArr = str.split(" ");
    for (let i = 0; i < strArr.length; i++) {
       if( !(i%2) ){
           arr.push(strArr[i].split(''));
       } else {
           for(let j=0;j<strArr[i].length;j++){
               var column = [' ',' ',' ',' ',' '];
               var tempindex = strArr[0].length;
               column[tempindex] = strArr[i][j];
               arr.push(column);
           }
       }
    }
    console.log(arr);
    return arr
}

